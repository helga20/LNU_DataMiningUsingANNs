% Ілюстрація як мережі GRNN вирішують завдання апроксимації. 

% Визначимо навчальну множину у вигляді масивів Р і Т
P = [1 2 3 4 5 6 7 8];
T = [0 1 2 3 2 1 2 1];

spread = 0.7; % Параметр впливу
net = newgrnn (P, T, spread);
net.layers{1}.size % Число нейронів у прихованому прошарку

A = sim (net, P);
plot(P,T,'*k','markersize',10)
hold on,
plot(P,A,'ok','markersize',10);