% Будова мережі GRNN

% Навчальні дані
P = [4 5 6]; % Множина векторів входу
T = [1.5 3.6 6.7]; % Множина векторів цілей
net = newgrnn(P,T);
net.layers{1}.size % Число нейронів у прихованому прошарку

% Модельовані дані для одного входу
p = 4.5;
v = sim (net, p);

% Модельовані дані для послідовності входів з інтервалу [4 7]
p1 = 4: 0.1: 7;
v1 = sim (net, p1);

plot(P, T, '*k', p, v, 'ok', p1, v1, '-k', 'MarkerSize', 10, 'LineWidth', 2);
legend('P, T', 'p*, v*', 'p, v');
